import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const envPath = path.join(root, ".env.local");
const outPath = path.join(root, "src", "env.secret.js");

if (!fs.existsSync(envPath)) {
  console.error("Missing .env.local in dist/extension");
  process.exit(1);
}

const raw = fs.readFileSync(envPath, "utf8");
const vars = {};

for (const line of raw.split(/\r?\n/)) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith("#")) continue;
  const eq = trimmed.indexOf("=");
  if (eq === -1) continue;
  const key = trimmed.slice(0, eq).trim();
  let value = trimmed.slice(eq + 1).trim();
  if ((value.startsWith("\"") && value.endsWith("\"")) || (value.startsWith("'") && value.endsWith("'"))) {
    value = value.slice(1, -1);
  }
  vars[key] = value;
}

const keys = ["OPENAI_API_KEY", "ANTHROPIC_API_KEY", "GOOGLE_API_KEY", "DEEPSEEK_API_KEY"];
const body = keys
  .map((key) => `export const ${key} = ${JSON.stringify(vars[key] || "")};`)
  .join("\n");

const file = `// AUTO-GENERATED by scripts/sync-env-from-local.mjs
// Do not commit this file if it contains real API keys.
${body}
`;

fs.writeFileSync(outPath, file, "utf8");
console.log(`Wrote ${path.relative(root, outPath)}`);
